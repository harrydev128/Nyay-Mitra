// Helper function to get translated text
export const t = (key: string, language: 'hi' | 'en' | 'hindi' | 'english', replacements?: { [key: string]: string }) => {
  const normalizedLang = language === 'hi' || language === 'hindi' ? 'hi' : 'en';
  let text = translations[key]?.[normalizedLang] || key;
  
  // Replace placeholders like {username}
  if (replacements) {
    Object.keys(replacements).forEach(placeholder => {
      text = text.replace(new RegExp(`{${placeholder}}`, 'g'), replacements[placeholder]);
    });
  }
  
  return text;
};
